public class SwitchingOptions{
    public static void main(String[] args) {
        int i = (0);

        switch(i) {
            case 0:
                System.out.println("Print one!");
             break;
            case 1:
                System.out.println("Print two!");
             break;
            case 2:
                System.out.println("Print three");
                break;
            default:
                System.out.println("wtf man??");
                break;
        }
    }
}
